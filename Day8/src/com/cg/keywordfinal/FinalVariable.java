//final variable
package com.cg.keywordfinal;
public class FinalVariable {
	//final variable must be initialized
	
	//when we use a final variable, we can't change the value of that variable
	final int x = 12;
	
	public void print() {
		//we can't change the value of final variable
		//x=13;
		System.out.println("The value of x is: "+ x);
	}

}
