package com.cg.mapinterface;

class Student {
    String name;
    int rollno;
   

    public Student(String name, int rollno) {
        this.name = name;
        this.rollno = rollno;
       
    }

    public String getName() {
        return name;
    }

    public int getRollno() {
        return rollno;
    }

 
}
